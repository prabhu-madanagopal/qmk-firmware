enum layers {
    DVORAK   = 0,    //   * Dvorak base layer.
    SYMBOLS  = 1,    //   * Symbols and arrows.
    NUMPAD   = 2,    //   * ATM style numpad with symbols and letters that should suffice to input any numeric literal.
    MEDIA_FN = 3,     //   * Media, RGB and function keys from F1 to F24 in symmetric fashion.
    MOUSE    = 4     //   * Mouse keys
};
